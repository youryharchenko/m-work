log = let
	logs_dir = joinpath("..", "Big", "logs", "nginx")
	files = []
	records = String[]
	log = []
	for file in filter(x -> occursin("access", x) && occursin("gz", x), readdir(logs_dir, join=true))
		push!(files, file)
		fh = GZip.open(file)
		ls = readlines(fh)
		append!(records, [replace(l, "("=>"", ")"=>"") for l in ls])
	end
	
	for file in filter(x -> occursin("access", x) && !occursin("gz", x), readdir(logs_dir, join=true))
		push!(files, file)
		fh = open(file)
		ls = readlines(fh)
		append!(records, [replace(l, "("=>"", ")"=>"") for l in ls])
	end

	
	types=[String,String,String,DateTime,String,Int,Int,String,String]
	
	df = CSV.File(IOBuffer.(records); header=header1, delim=' ',types=types, silencewarnings=true, dateformat="[d/u/yyyy:H:M:S +0000]") |> DataFrame

	select(df, header)
	
end

