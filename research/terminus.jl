function communicate(cmd::Cmd, input="")
    inp = Pipe()
    out = Pipe()
    err = Pipe()

    process = run(pipeline(cmd, stdin=inp, stdout=out, stderr=err), wait=false)
    close(out.in)
    close(err.in)

    stdout = @async String(read(out))
    stderr = @async String(read(err))
    write(process, input)
    close(inp)
    wait(process)
    return (
        stdout = fetch(stdout),
        stderr = fetch(stderr),
        code = process.exitcode
    )
end

# ╔═╡ 8762837a-8d02-11ed-019b-81972156e264
function exec_print_out(cmd, inp = "")
	res = communicate(cmd, inp)
	Text(res.stdout)
end

